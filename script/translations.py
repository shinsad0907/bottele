def t(key: str, lang: str = DEFAULT_LANG, **kwargs) -> str:
    """Return translated string for *key* in *lang*, falling back to English."""
    table = _T.get(lang, _T[DEFAULT_LANG])
    text  = table.get(key, _T[DEFAULT_LANG].get(key, f"[{key}]"))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def get_lang_name(code: str) -> str:
    """Return display label like '🇬🇧 English (English)' or '🇻🇳 Vietnamese (Tiếng Việt)'."""
    info   = LANGUAGES.get(code, LANGUAGES["en"])
    flag   = info["flag"]
    name   = info["name"]
    native = info["native"]
    if name == native:
        return f"{flag} {name}"
    return f"{flag} {name} ({native})"


def lang_keyboard(current_lang: str = DEFAULT_LANG) -> InlineKeyboardMarkup:
    """
    Return a 2-column inline keyboard of all languages sorted A→Z.
    Current language is marked with ✅.
    Last row has a Back button (callback: 'home').
    """
    codes = sorted(LANGUAGES.keys(), key=lambda c: LANGUAGES[c]["name"])
    rows: list = []
    for i in range(0, len(codes), 2):
        row = []
        for code in codes[i : i + 2]:
            label = get_lang_name(code)
            if code == current_lang:
                label = f"✅ {label}"
            row.append(InlineKeyboardButton(label, callback_data=f"lang_set_{code}"))
        rows.append(row)
    rows.append([InlineKeyboardButton("◀️ Back", callback_data="home")])
    return InlineKeyboardMarkup(rows)